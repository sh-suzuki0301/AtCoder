 */

$count = 0;
for ($i=0; $i<$n-1; $i++) { // １個目の座標
    for ($j=$i+1; $j<$n; $j++) { // 2個目の座標
        $x = $xy[$j][0] - $xy[$i][0]; // xの増加分(次のx座標 - 前のx座標)
        $y = $xy[$j][1] - $xy[$i][1]; // yの増加分((次のy座標 - 前のy座標)
        if ($y / $x >= -1 && $y / $x <= 1) { // −1以上1以下の傾き
            $count++;
        }
    }
}

echo $count;
